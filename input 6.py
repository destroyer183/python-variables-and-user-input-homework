number1 = int(input("First Number: "))
number2 = int(input("Second Number: "))

sum = int(number1) + int(number2)
difference = int(number1) - int(number2)
product = int(number1) * int(number2)
quotient = int(number1) / int(number2)

print(str(number1)  + " + " + str(number2)  + " = " + str(sum))
print(str(number1)  + " - " + str(number2)  + " = " + str(difference))
print(str(number1)  + " x " + str(number2)  + " = " + str(product))
print(str(number1)  + " / " + str(number2)  + " = " + str(quotient))